package com.hermes.hanbakwi.vo;

public class Room {

	private int rmNo,siteNo;
	private String thumbnail,name,basicPeople,addPeople,basicPrice,addPrice,timePrice;
	
	public Room() {
		
	}

	public int getRmNo() {
		return rmNo;
	}

	public void setRmNo(int rmNo) {
		this.rmNo = rmNo;
	}

	public int getSiteNo() {
		return siteNo;
	}

	public void setSiteNo(int siteNo) {
		this.siteNo = siteNo;
	}

	public String getThumbnail() {
		return thumbnail;
	}

	public void setThumbnail(String thumbnail) {
		this.thumbnail = thumbnail;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getBasicPeople() {
		return basicPeople;
	}

	public void setBasicPeople(String basicPeople) {
		this.basicPeople = basicPeople;
	}

	public String getAddPeople() {
		return addPeople;
	}

	public void setAddPeople(String addPeople) {
		this.addPeople = addPeople;
	}

	public String getBasicPrice() {
		return basicPrice;
	}

	public void setBasicPrice(String basicPrice) {
		this.basicPrice = basicPrice;
	}

	public String getAddPrice() {
		return addPrice;
	}

	public void setAddPrice(String addPrice) {
		this.addPrice = addPrice;
	}

	public String getTimePrice() {
		return timePrice;
	}

	public void setTimePrice(String timePrice) {
		this.timePrice = timePrice;
	}
	
}
